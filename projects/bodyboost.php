                <h3 class="question">Pre-Production</h3>
                <p class="answer">Before the designers opened their laptops, we had to sit down as a team and determine what needed to be done to complete the deliverables, who wanted to focus on what, and how long we had to complete tasks. This process took two meetings before we understood the outlook for the project. With this team consultation complete, I had to put the plan down on paper and set up a Work Breakdown Structure.</p>
                
                <p class="answer"><strong>Work Breakdown Structure</strong></p>
                <p class="answer">A Work Breakdown Structure (WBS) is an exhaustive list of every task or item that must be completed within the project's scope. I had to put myself in the shoes of each role and speak the language of each designer to ensure I included every step of the process.</p>
                <img src="../media/img/projects/bodyboost/bodyboost5.jpeg" class="project-img" alt="Body Boost Project - Artifact Image">
                <img src="../media/img/projects/bodyboost/bodyboost6.jpeg" class="project-img" alt="Body Boost Project - Artifact Image">

                
                <p class="answer"><strong>Gantt Chart</strong></p>
                <p class="answer">After the Work Breakdown Structure had been finalized, I reinterpreted the list into a Gantt Chart where the items are displayed based on when they will be completed. The items are listed along the left y-axis and their start/end dates are plotted on the x-axis. On this chart, the tasks are grouped by assignee and colour-coded accordingly. Each task/item occupies cells in the graph which denotes when this task will be worked on and completed.</p>

                <img src="../media/img/projects/bodyboost/bodyboost7.jpeg" class="project-img" alt="Body Boost Project - Artifact Image">

                <p class="answer">It took over 7 hours of planning to ensure teammates had enough time to complete their tasks while ensuring they weren't working on two items simultaneously, and watching for dependencies.<br><br>Dependencies are items that must be completed before another begins. Dependencies must be addressed effectively to avoid designers sitting idle waiting for another task to finish. It was fun to come up with unique solutions to fix this problem! My teammates thought I was a little crazy when I presented the Gantt to them! For example, I planned to begin wireframes for both the website and email template before we had determined the brand colours or logo. I made this decision because designers only need the final colours very late in the design process for things like email templates, so this work can happen simultaneously, keeping our project efficient, dynamic and a little fun too!<br><br>This was a daunting challenge to look ahead for all issues that could come up before we had even touched the projectâ€™s deliverables. I really enjoyed the critical thinking and collaboration that is required.<br><br><br>In addition to these two documents, I set up a change log, and budget tracking documents to help us with the final report and retrospective project analysis.</p>
                
